public class Bahan {
    public int idba;
    public String namaba;
    public String jenisba;

    public int getIdba() {
        return idba;
    }

    public void setIdba(int idba) {
        this.idba = idba;
    }

    public String getNamaba() {
        return namaba;
    }

    public void setNamaba(String namaba) {
        this.namaba = namaba;
    }


    public String getJenisba() {
        return jenisba;
    }

    public void setJenisba(String jenisba) {
        this.jenisba = jenisba;
    }
}
